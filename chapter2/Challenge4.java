package chapter2;

/* 4. Star Pattern
   Write a program that displays the following pattern:
 				*
 			   ***
 			  *****
 			 *******
 			  *****
 			   ***
 			    *
 */

public class Challenge4 {

	public static void main(String[] args) {
		System.out.println("    *    ");
		System.out.println("   ***   ");
		System.out.println("  *****  ");
		System.out.println(" ******* ");
		System.out.println("  *****  ");
		System.out.println("   ***   ");
		System.out.println("    *    ");
	}
}
